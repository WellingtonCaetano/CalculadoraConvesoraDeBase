    private void jButtonConverterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConverterActionPerformed
        // TODO add your handling code here:
     

        try {
         
            
            if (jTextFieldSaida.getText().equals(null) || jTextFieldSaida.getText().equals("")
                    || jTextFieldSaida.getText().equals("0")) {
                JOptionPane.showMessageDialog(null, "Favor informar um numero para conversão!");
            } else if (calc.getBase1() == calc.getBase2()) {
                JOptionPane.showMessageDialog(null, "As Bases selecionadas são iguais \n" + "Selecione uma base diferente para conversão!");
            } else {
                String N1 = jTextFieldSaida.getText();
                calc.setN1(N1);
                          
               jTextFieldSaida.setText(calc.mudarBase());
        

                calculado = true;
                jTextFieldSaida.grabFocus();
                 
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
      
    }//GEN-LAST:event_jButtonConverterActionPerformed

    private void jButtonVoltarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVoltarActionPerformed
        // TODO add your handling code here:
        String voltar = jTextFieldSaida.getText();
        if (voltar.length() == 1) {
            jTextFieldSaida.setText("0");
        } else {
            jTextFieldSaida.setText(voltar.substring(0, voltar.length() - 1));
        }
        jTextFieldSaida.grabFocus();

    }//GEN-LAST:event_jButtonVoltarActionPerformed

