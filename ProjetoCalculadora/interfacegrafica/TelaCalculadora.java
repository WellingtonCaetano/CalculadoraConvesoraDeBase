    private void jTextFieldSaidaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldSaidaKeyTyped
        // TODO add your handling code here:
        String caracteres = "";

        if (calc.getBase1() == 16) {
            caracteres = "0123456789ABCDEF";
        } else if (calc.getBase1() == 10) {
            caracteres = "0123456789";
        } else if (calc.getBase1() == 8) {
            caracteres = "01234567";
        } else if (calc.getBase1() == 2) {
            caracteres = "01";
        }

        if (!caracteres.contains(evt.getKeyChar() + "")) {
            evt.consume();

        }
    }//GEN-LAST:event_jTextFieldSaidaKeyTyped

